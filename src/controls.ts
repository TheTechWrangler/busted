document.addEventListener('keydown', (e) => {
  switch (e.code) {
    case 'KeyW':
      controls.forward = true;
      break;
    case 'KeyS':
      controls.backward = true;
      break;
    case 'KeyA':
      controls.left = true;
      break;
    case 'KeyD':
      controls.right = true;
      break;
    case 'Space':
      controls.jump = true;
      break;
    case 'ControlLeft':
    case 'ControlRight':
      controls.descend = true;
      break;
    case 'KeyE':
      mountCallbacks.forEach((fn) => fn());
      break;
    case 'KeyR':
      inventory.clear();
      break;
  }
});

document.addEventListener('keyup', (e) => {
  switch (e.code) {
    case 'KeyW':
      controls.forward = false;
      break;
    case 'KeyS':
      controls.backward = false;
      break;
    case 'KeyA':
      controls.left = false;
      break;
    case 'KeyD':
      controls.right = false;
      break;
    case 'Space':
      controls.jump = false;
      break;
    case 'ControlLeft':
    case 'ControlRight':
      controls.descend = false;
      break;
    case 'Digit1':
    case 'Digit2':
    case 'Digit3':
    case 'Digit4':
    case 'Digit5':
    case 'Digit6':
    case 'Digit7':
    case 'Digit8':
    case 'Digit9':
    case 'Digit0': {
      const index = (parseInt(e.code.replace('Digit', '')) + 9) % 10; // 1-0 maps to 0-9
      const item = hotbar.getSlots()[index];
      if (item) {
        console.log(`[Hotbar] Using item in slot ${index}:`, item);
        // TODO: Hook up item usage here
      } else {
        console.log(`[Hotbar] Slot ${index} is empty`);
      }

      // ğŸ”¶ Highlight selected slot using CSS class
      document.querySelectorAll('.hotbar-slot').forEach(slot => {
        (slot as HTMLElement).classList.remove('selected');
      });

      const selectedSlot = document.querySelector(`.hotbar-slot[data-index="${index}"]`) as HTMLElement;
      if (selectedSlot) {
        selectedSlot.classList.add('selected');
      }

      break;
    }
  }
});
